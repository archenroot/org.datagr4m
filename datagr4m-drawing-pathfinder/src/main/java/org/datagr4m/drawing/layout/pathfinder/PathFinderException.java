package org.datagr4m.drawing.layout.pathfinder;

public class PathFinderException extends Exception{
    public PathFinderException() {
        super();
    }

    public PathFinderException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public PathFinderException(String arg0) {
        super(arg0);
    }

    public PathFinderException(Throwable arg0) {
        super(arg0);
    }

    private static final long serialVersionUID = 3714240200897407849L;
}
